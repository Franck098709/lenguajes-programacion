float circulo(float valorA)
{
    return (valorA * valorA) * 3.1416;
}

float pentagono(float valorA, float valorB)
{
    return ((valorA * valorB) / 2);
}

float trapecio(float valorA, float valorB, float valorC)
{
    return ((valorA + valorB) / 2) * valorC;
}

float rectangulo(float valorA, float valorB)
{
    return valorA * valorB;
}
